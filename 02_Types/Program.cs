// Bool is a true false integer only, no numbers or other characters
// True and false are keywords, not strings
bool isLoggedIn = true;
Console.WriteLine (isLoggedIn);

// Integers are whole numbers only
int age = 42;
Console.WriteLine(age);

double balanceDue = 800.99d;
Console.WriteLine(balanceDue);

// Decimal can have a lot of possible decimal points

decimal accountBalance = 9000.01m;
Console.WriteLine(accountBalance);

float averageHeight = 5.9f;
Console.WriteLine(averageHeight);